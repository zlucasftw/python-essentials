# Sorting

# A segunda forma de ordenar uma lista é utilizando um método
# que afeta a lista, nenhuma lista nova é criada e a ordenação
# é realizada na própria lista em tempo de execução com o
# método sort()

# Um detalhe é que existem formas de que caso se deseja que a ordem
# seja decrescente, precisa-se alterar o comportamento padrão
# do método ou função.

# Demonstrating the sort() method:
second_greek = ['omega', 'alpha', 'pi', 'gamma']
print(second_greek)

second_greek.sort()
print(second_greek)
