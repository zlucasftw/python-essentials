# Strings em ação

# Comparar strings

# Não se é uma boa ideia comparar strings com números.
# As únicas comparações que se pode realizar são as de igualdade (==)
# ou diferença (!=), a utilização de qualquer um dos outros operadores
# de comparação irá disparar um erro ou exceção.

print('10' == 10)
print('10' != 10)
print('10' == 1)
print('10' != 1)
print('10' > 10)
