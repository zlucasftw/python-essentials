# Strings em ação

# Strings vs. números

# Para converter um número, sendo inteiro ou um float e vice-versa,
# uma questão rotineira no processamento de dados de input/output.
# A conversão número-string é simples, pois é sempre possível
# e é feita através da função chamada str().

itg = 13
flt = 1.3
si = str(itg)
sf = str(flt)

print(si + ' ' + sf)
