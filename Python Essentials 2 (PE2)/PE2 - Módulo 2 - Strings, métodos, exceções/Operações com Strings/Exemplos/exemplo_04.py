# Comparar strings

# Mesmo que uma string contenha apenas dígitos não é um número.
# É interpretada como qualquer outra string regular e o
# aspecto número não é tomado em consideração.

print('10' == '010')
print('10' > '010')
print('10' > '8')
print('20' < '8')
print('20' < '80')
