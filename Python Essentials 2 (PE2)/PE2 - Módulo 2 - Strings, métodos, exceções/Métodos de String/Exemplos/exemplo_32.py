# Métodos de String

# Método swapcase()

# O método swapcase() cria uma nova string a partir de uma original
# em que troca todas as letras maiúsculas e minúsculas que a string original possui.
# Todos caracteres minúsculos tornam-se maiúsculos e vice-versa.

# Demonstrating the swapcase() method:
print("I know that I know nothing.".swapcase())
