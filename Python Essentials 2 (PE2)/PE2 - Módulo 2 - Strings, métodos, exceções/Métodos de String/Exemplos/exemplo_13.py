# Métodos de String

# Método isalnum()

# O método isalnum() é um método sem parâmetros e verifica
# se a string contém apenas dígitos ou caracteres alfabéticos
# (letras) e retorna True ou False de acordo com o resultado.

# Qualquer elemento da string que não seja um dígito
# ou uma letra, assim como uma string vazia retornará
# False.

# Demonstrating the isalnum() method:
print('lambda30'.isalnum())
print('lambda'.isalnum())
print('30'.isalnum())
print('@'.isalnum())
print('lamba_30'.isalnum())
print(''.isalnum())
