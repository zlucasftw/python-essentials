# Métodos de String

# Método join()

# O método join() realiza uma junção esperando como argumento
# uma lista em que se deve ter a certeza em que todos
# elementos são strings caso contrário irá disparar
# uma exceção. Todos os elementos da lista serão unidos
# em uma string e a string a partir do qual o método é invocado
# é utilizado como separador colocado entre as strings.
# O método retorna a string criada como resultado.

# Demonstrating the join() method:
print(",".join(["omicron", "pi", "rho"]))
