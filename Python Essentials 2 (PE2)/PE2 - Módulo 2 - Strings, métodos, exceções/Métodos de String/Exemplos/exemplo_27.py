# Métodos de String

# Método rfind()

# O método rfind() é um método com até três parâmetros e faz o
# mesmo que métodos similares sem o prefixo r, a diferença
# é que inicia sua busca a partir do fim da string, não do início.
# O prefixo r indica right, direita.

# Demonstrating the rfind() method:
print("tau tau tau".rfind("ta"))
print("tau tau tau".rfind("ta", 9))
print("tau tau tau".rfind("ta", 3, 9))
