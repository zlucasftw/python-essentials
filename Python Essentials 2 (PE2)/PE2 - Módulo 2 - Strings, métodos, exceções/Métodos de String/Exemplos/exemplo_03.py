# Métodos de String

# Método center()

# O método center() faz uma cópia da string original
# e tenta centralizá-la dentro de um campo de uma
# largura especificada.
# A centralização é feita adicionando espaços antes
# e depois da string. Recebe como argumento
# a largura especificada.

# Demonstrating the center() method:
print('[' + 'alpha'.center(10) + ']')
