# Métodos de String

# Método split()

# Método split() divide toda uma string e constrói uma lista
# de todas os caracteres ou substrings detectados.
# Esse método assume que as substring são delimitadas por espaços
# em branco, onde os espaços não serão copiados para a lista
# resultante e, se a string estiver vazia a lista resultante
# também estará vazia. Pode-se fazer a operação inversa
# através do método join().

# Demonstrating the split() method:
print("phi      chi\npsi".split())
