# Métodos de String

# Método find()

# Há também uma variação do método find() que possui três parâmetros,
# onde o terceiro argumento aponta  para o primeiro índice que
# não será tomado em consideração durante a pesquisa.
# É o limite superior da pesquisa.

print('kappa'.find('a', 1, 4))
print('kappa'.find('a', 2, 4))
