# Métodos de String

# Método find()

# O método find() é semelhante ao index(), procura um caracter
# e retorna o índice da primeira ocorrência dele entretanto
# em diferença do método index() é mais seguro, não gera
# um erro para um argumento que contém um caracter inexistente
# devolvendo -1 no lugar e funciona apenas com strings.

# Demonstrating the find() method:
print("Eta".find("ta"))
print("Eta".find("mma"))
