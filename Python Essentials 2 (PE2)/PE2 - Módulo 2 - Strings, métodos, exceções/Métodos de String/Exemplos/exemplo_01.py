# Métodos de String

# Método capitalize()

# Cria uma nova string a partir de caracteres de uma outra string
# modificando-os se, o primeiro caractere da string é uma letra,
# primeiro sendo visto através de índices sendo o índice 0,
# e o convertendo para uma letra maiúscula. Todo o restante da string
# é convertido em letras minúsculas.

# A string original não é alterada da qual o método é invocado,
# é apenas retornado como resultado a string modificada
# para atribuí-lá a uma variável ou utilizá-la de alguma forma.

# Demonstrating the capitalize() method:
print('aBcD'.capitalize())
