# Métodos de String

# Método lower()

# O método lower() não tem recebe nenhum parâmetro,
# faz uma cópia de uma string, e substitui
# todas as letras maiúsculas da cópia por minúsculas, devolvendo-a
# como resultado, deixando a string original intocada.

# Caso a string não contiver caracteres maiúsculos,
# o método devolve a string original

# Demonstrating the lower() method:
print("SiGmA=60".lower())
