# Resumo da seccção - Key takeaways

# 1. Alguns dos métodos oferecidos por strings são:

# capitalize() - altera todas as letras da string para maiúsculas
# center() - centraliza a string dentro do campo de um comprimento conhecido
# count() - conta as ocorrências de um determinado caractere
# join() - junta todos os itens de uma tupla ou lista em uma string
# lower() - converte todas as letras da string em letras minúsculas
# lstrip() - remover os caracteres brancos a partir do início ou da esquerda da string
# replace() - substitui uma determinada substring ou caracter por outro
# rfind() - encontra um caracter ou substring a partir da direita ou final da string
# rstrip() - remove os espaços em brancos à direita do final da string
# split() - divide a string em uma substring usando um determinado delimitador
# strip() - remove espaços em brancos da string tanto à esquerda quanto à direita
# swapcase() - troca letras maiúsculas e minúsculas (minúsculas para maiúsculas e vice-versa)
# title() - torna a primeira letra de cada palavra uma letra maiúscula
# upper() - converte todas as letras de uma string para letras maiúsculas
