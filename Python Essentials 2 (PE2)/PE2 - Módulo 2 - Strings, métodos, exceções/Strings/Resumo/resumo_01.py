# Resumo da secção - Key takeaways

# Strings no Python são sequências imutáveis que
# podem ser indexadas, sliced (cortadas) e iteradas
# como qualquer outra sequência. São sujeitas aos
# operadores in e not in.

# Existem dois tipos de Strings no Python, one-line
# que não cruzam limites de linha e denotadas por apóstrofes ou aspas,
# e strings multi-lin, que ocupam mais de uma linha e são delimitadas
# por trígrafos

# Exemplos one-line

'string'

"string"

# Exemplos multi-line

'''
string
'''

"""
string
"""
