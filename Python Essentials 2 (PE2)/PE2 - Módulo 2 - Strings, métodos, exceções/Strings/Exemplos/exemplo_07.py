# A natureza das strings em Python

# Operações em strings

# Strings podem ser concatenadas pelo operador (+) ou replicadas pelo operador (*)
# Utilizar o mesmo operador contra tipos de dados diferentes como números
# e strings se chama overloading
# Variantes resumidas dos operadoras (+=) e (*=) também são aplicáveis para as strings

str1 = 'a'
str2 = 'b'

print(str1 + str2)
print(str2 + str1)
print(5 * 'a')
print('b' * 4)
