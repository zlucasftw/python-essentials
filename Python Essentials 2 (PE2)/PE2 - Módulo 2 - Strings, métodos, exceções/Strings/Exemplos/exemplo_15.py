# A natureza das strings em Python

# Strings em Python são imutáveis.

# Operações que se podem fazer em lista não pode
# ser feita com uma string.
# Não é permitido utilizar a instrução 'del' para remover
# algum caracter de uma string, somente a string inteira.

alphabet = "abcdefghijklmnopqrstuvwxyz"
del alphabet[0]
