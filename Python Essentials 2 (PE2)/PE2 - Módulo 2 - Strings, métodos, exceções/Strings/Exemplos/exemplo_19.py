# A natureza das strings em Python

# Operações em strings: min()

# Assim como nas listas a função min() encontra o elemento
# mínimo da sequência que é passada como argumento.

# Demonstrating min() - Example 1:
print(min("aAByYzZ"))

# Demonstrating min() - Examples 2 &amp; 3:
t = 'The Knights Who Say "Ni!"'
print('[' + min(t) + ']')

t = [0, 1, 2]
print(min(t))
