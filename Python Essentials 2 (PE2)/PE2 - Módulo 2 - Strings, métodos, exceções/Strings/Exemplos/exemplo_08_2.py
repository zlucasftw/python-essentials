# A natureza das strings em Python

# Operações em strings: ord()

# Pode-se utilizar a funçã ord() para saber o valor
# do code point ASCII/UNICODE de um caractere específico
# A função só recebe apenas um caractere.

# Demonstrating the ord() function.

char_1 = 'α'    # alfa grego
char_2 = 'ę'    # letra no alfabeto polônes

print(ord(char_1))
print(ord(char_2))
