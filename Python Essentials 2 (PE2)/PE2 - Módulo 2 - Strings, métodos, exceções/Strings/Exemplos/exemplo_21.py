# A natureza das strings em Python

# Operações em strings: método index()

# O método index() pesquisa a sequência desde o início
# ao fim para encontrar o primeiro elemento
# do valor especificado no seu argumento.
# O método devolve o índice da primeira ocorrência
# do argumento.

# Demonstrating the index() method:
print("aAbByYzZaA".index("b"))
print("aAbByYzZaA".index("Z"))
print("aAbByYzZaA".index("A"))
