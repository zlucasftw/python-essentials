# A natureza das Strings em Python

# Os loops com os operadores in e not in

# O operador in - quando aplicado a strings
# verifica se o argumento esquerdo, uma string,
# pode ser encontrado em qualquer lugar dentro do
# argumento direito (outra string)
# O resultado da verifição é True e False

alphabet = "abcdefghijklmnopqrstuvwxyz"

print("f" in alphabet)
print("F" in alphabet)
print("1" in alphabet)
print("ghi" in alphabet)
print("Xyz" in alphabet)
