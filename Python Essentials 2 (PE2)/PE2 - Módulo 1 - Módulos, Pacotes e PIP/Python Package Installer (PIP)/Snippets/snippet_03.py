# Instalação do pip em Linux

# Algumas distribuições do Linux não tem o pip pré-instalado
# mesmo que o Python já esteja instalado. Pode-se então utilizar um
# gestor de pacotes dedicado da distribuição ou usando mecanismos
# internos do próprio Python

# Verifica a versão do Python
# python --version

# Caso exista o Python 2 e 3 instalados simultaneamente para verificar a versão do Python 3
# python3 --version

# Comando que pode variar para cada distribuição do Linux
# devido ao uso de diferentes gestores de pacotes
# ('apt' em distrbuições derivadas do tipo Debian, 'pacman' para Arch Linux,
# 'yum' para distribuições derivadas da Red Hat)

# sudo apt install python3-pip
