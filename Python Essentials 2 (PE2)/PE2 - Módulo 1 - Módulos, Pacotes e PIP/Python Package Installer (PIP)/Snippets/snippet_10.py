# Como utilizar o pip

# Pode-se utilizar o mesmo comando de ver mais
# informações sobre os pacotes instalados
# para o próprio pip com:

# pip show pip

# Os dados que são mostrados com o comando são retirados
# do interior do pacote informado, e o criador do pacote
# é 'obrigado' a deixá-lo com todos os dados necessários

# Uma das linhas por exemplo mostra quais pacotes são
# necessários para utilizar o pacote informado: 'Requires:'

# Outra linha é quais os pacotes que precisam do pacote informado
# necessariamente para serem utilizados: 'Required-by:'
