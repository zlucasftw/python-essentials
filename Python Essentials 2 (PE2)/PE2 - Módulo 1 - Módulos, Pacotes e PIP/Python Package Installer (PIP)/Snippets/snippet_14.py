# Como utilizar o pip

# Instalação do pacote chamado pygame.
# É uma biblioteca extensa e complexa que permite
# a programadores desenvolver jogos usando Python.
# É um projeto que está em desenvolvimento desde 2000,
# possuindo um código muito maduro e com uma comunidade
# ativa e que o lidera.

# O comando para instalar o pygame com o pip de maneira global
# como administrador do sistema:

# pip install pygame

# Caso não seja administrador do sistema ou não se quer o pygame em
# todo o sistema operacional pode instalar apenas para o utilizador:

# pip install --user pygame
