# Como utilizar o pip

# O pip utiliza a internet para consultar o PyPI
# significando que sempre precisará de uma ligação à
# internet ao pedir o pip alguma operação que envolva
# interações diretas com o PyPI que é um repositório
# com centenas de pacotes em que o pip utiliza.

# Para pesquisar através do PyPI um pacote desejado
# pode-se utilizar o seguinte comanddo:

# pip search anystring

# Sendo 'anystring' o nome do pacote, e
# será pesquisado pelos nomes de todos os pacotes
# e o texto de sumário de todos os pacotes

# Entretanto o comando foi retirado de suporte e utilização recentemente.
