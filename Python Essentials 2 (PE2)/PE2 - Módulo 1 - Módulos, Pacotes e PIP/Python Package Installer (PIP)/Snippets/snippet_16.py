# Como utilizar o pip

# O comando 'pip install' tem duas capacidades adicionais

# É capaz de atualizar um pacotes instalado localmente
# através do comando:

# pip install -U package_name

# '-U' significa atualização, ou seja, o comando
# fez uso de uma opção da mesma maneira que '--user'
# também pode ser feito

# É capaz de instalar uma versão do pacote
# escolhida pelo utilizador.
# O pip instala por padrão a versão mais recente.
# Para esse objetivo segue-se o comando:

# pip install package_name==package_version

# O mesmo comando para o pacote pygame

# pip install pygame==1.9.2
