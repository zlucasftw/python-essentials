# Como utilizar o pip

# Pesquisar com o pip através do PyPI poderia trazer
# grandes quantidades de dados e informações,
# sendo uma boa prática ser o mais específico possível

# O comando seguinte produzia mais de 100 linhas de resultado
# e a pesquisa é case insensitive.

# pip search pip

# Entretanto o comando foi retirado de suporte e utilização recentemente.
# Agora pode-se navegar pelo conteúdo do PyPI pelo próprio website.
