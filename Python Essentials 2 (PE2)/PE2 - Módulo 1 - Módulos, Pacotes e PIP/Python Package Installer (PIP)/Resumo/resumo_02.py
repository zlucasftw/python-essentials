# Comandos principais do pip

# pip help operation - mostra uma descrição do pip
# pip list - mostra a lista de pacotes que estão instalados
# pip show 'package_name' - mostra informações do pacote informado e suas dependências
# pip search 'anystring' - realiza uma pesquisa no PyPI para encontrar um pacote que corresponda ao texto ou string
# pip install 'name' - instala o pacote informado em todo sistema ou de maneira global
# pip install --user 'name' - instala o pacote informado apenas para o usuário atual que o está utilizando
# pip install -U 'name' - atualiza o pacote informado que deve estar instalado.
# pip uninstall 'name' - desinstala o pacote informado se estiver instalado.
