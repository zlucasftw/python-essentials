# Qual é o output do seguinte snippet?

tup = 1, 2, 3
a, b, c = tup

print(a * b * c)

# Resposta: (1, 2, 3) x 4
# Resposta incorreta pois os valores ou elementos da tuple são distribuidos respectivamente para
# cada variável respectivamente que possuem o mesmo tamanho, e por resultado será a multiplicação
# de 1 * 2 * 3 que é o resultado é 6
