# Número inteiro aleatório através da função randrange()

# Instrução from-import fornece acesso à função randrange definida dentro de um módulo
# externo do Python chamado random

# Impressão de números aleatórios de 0 a 8 dez vezes

from random import randrange

for i in range(10):
    print(randrange(8))
