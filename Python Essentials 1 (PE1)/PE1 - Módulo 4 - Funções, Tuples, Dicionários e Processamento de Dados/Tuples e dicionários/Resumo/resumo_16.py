# 5. Pode-se utilizar a estrutura de repetição/loop for para percorrer um dicionário

pol_eng_dictionary = {
    "zamek": "castle",
    "woda": "water",
    "gleba": "soil"
    }

for item in pol_eng_dictionary:
    print(item)

# outputs: zamek
#          woda
#          gleba
